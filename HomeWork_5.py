from colorama import Fore, Style, init

# Инициализируем colorama
init()

# Эта библиотека нужна для изменения цвета текста в консоли.
# Мы используем её, чтобы сделать вывод программы более ярким и понятным для пользователя.

print(Fore.YELLOW + "Привет! Это домашнее задание по внешним зависимостям.")
print(Fore.GREEN + "Библиотека colorama успешно установлена и работает!")
print(Style.RESET_ALL + "А этот текст снова обычного цвета.")